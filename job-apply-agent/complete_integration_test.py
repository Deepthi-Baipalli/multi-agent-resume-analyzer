def convert_numpy_types(obj):
    """Convert numpy types to native Python types for JSON serialization"""
    if isinstance(obj, dict):
        return {key: convert_numpy_types(value) for key, value in obj.items()}
    elif isinstance(obj, list):
        return [convert_numpy_types(item) for item in obj]
    elif isinstance(obj, (np.integer, np.int64, np.int32)):
        return int(obj)
    elif isinstance(obj, (np.floating, np.float64, np.float32)):
        return float(obj)
    elif isinstance(obj, np.ndarray):
        return obj.tolist()
    else:
        return obj
def save_all_materials_from_result(workflow_result, output_dir="saved_applications"):
    """
    Save ALL generated materials (cover letter, resume bullets, Q&A) from workflow result
    FIXED: Handles numpy float32/float64 types for JSON serialization
    """
    
    # Create output directory
    Path(output_dir).mkdir(exist_ok=True)
    
    # Extract data
    job_data = workflow_result.get('job', {})
    materials_data = workflow_result.get('materials', {})
    
    company = job_data.get('company', 'Unknown_Company').replace(' ', '_')
    job_title = job_data.get('title', 'Unknown_Position').replace(' ', '_')
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    
    saved_files = {}
    
    # 1. Save Cover Letter
    cover_letter = materials_data.get('cover_letter', '')
    if cover_letter:
        cover_filename = f"cover_letter_{company}_{job_title}_{timestamp}.txt"
        cover_path = os.path.join(output_dir, cover_filename)
        
        with open(cover_path, 'w', encoding='utf-8') as f:
            f.write(f"COVER LETTER - {job_data.get('title')} at {job_data.get('company')}\n")
            f.write("=" * 60 + "\n\n")
            f.write(cover_letter)
            f.write("\n\n" + "=" * 60)
            f.write(f"\nGenerated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
            
            # FIXED: Convert score to regular float
            score = workflow_result.get('score', {}).get('overall_score', 'N/A')
            if isinstance(score, (np.floating, np.float32, np.float64)):
                score = float(score)
            f.write(f"\nFit Score: {score}%")
            f.write("\nGenerated by Job Apply-Assist Agent")
        
        saved_files['cover_letter'] = cover_path
        print(f"✅ Cover letter saved: {cover_filename}")
    
    # 2. Save Resume Bullets
    resume_bullets = materials_data.get('resume_bullets', [])
    if resume_bullets:
        bullets_filename = f"resume_bullets_{company}_{job_title}_{timestamp}.txt"
        bullets_path = os.path.join(output_dir, bullets_filename)
        
        with open(bullets_path, 'w', encoding='utf-8') as f:
            f.write(f"RESUME BULLETS - {job_data.get('title')} at {job_data.get('company')}\n")
            f.write("=" * 60 + "\n\n")
            for i, bullet in enumerate(resume_bullets, 1):
                f.write(f"• {bullet}\n")
            f.write("\n" + "=" * 60)
            f.write(f"\nGenerated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        
        saved_files['resume_bullets'] = bullets_path
        print(f"✅ Resume bullets saved: {bullets_filename}")
    
    # 3. Save Q&A Answers
    qa_answers = materials_data.get('qa_answers', {})
    if qa_answers:
        qa_filename = f"qa_answers_{company}_{job_title}_{timestamp}.txt"
        qa_path = os.path.join(output_dir, qa_filename)
        
        with open(qa_path, 'w', encoding='utf-8') as f:
            f.write(f"Q&A ANSWERS - {job_data.get('title')} at {job_data.get('company')}\n")
            f.write("=" * 60 + "\n\n")
            for question, answer in qa_answers.items():
                f.write(f"Q: {question.replace('_', ' ').title()}\n")
                f.write(f"A: {answer}\n\n")
            f.write("=" * 60)
            f.write(f"\nGenerated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        
        saved_files['qa_answers'] = qa_path
        print(f"✅ Q&A answers saved: {qa_filename}")
    
    # 4. Save Complete Application Package (JSON) - FIXED
    package_filename = f"application_package_{company}_{job_title}_{timestamp}.json"
    package_path = os.path.join(output_dir, package_filename)
    
    # FIXED: Convert numpy types before JSON serialization
    application_package = {
        "job_details": convert_numpy_types(job_data),
        "materials": convert_numpy_types(materials_data),
        "fit_score": convert_numpy_types(workflow_result.get('score', {})),
        "generated_at": datetime.now().isoformat(),
        "files_created": saved_files
    }
    
    try:
        with open(package_path, 'w', encoding='utf-8') as f:
            json.dump(application_package, f, indent=2, ensure_ascii=False)
        
        saved_files['complete_package'] = package_path
        print(f"✅ Complete package saved: {package_filename}")
        
    except Exception as e:
        print(f"⚠️ Could not save JSON package: {str(e)}")
        # Continue anyway - the important files are already saved
    
    return saved_files

