def rank_jobs_for_profile(jobs, profile):
    """Rank jobs specifically for the candidate profile"""
    
    candidate_skills = [skill.lower() for skill in profile.get("skills", [])]
    candidate_experience = profile.get("years_experience", 0)
    
    # Sreecharan's specific skills
    ds_skills = ['python', 'machine learning', 'data science', 'pandas', 'pytorch', 'langchain']
    
    ranked_jobs = []
    
    for job in jobs:
        relevance_score = 0
        
        job_text = f"{job.get('title', '')} {job.get('description', '')}".lower()
        job_title = job.get('title', '').lower()
        
        # Score 1: Data Science specific skills (high weight)
        ds_matches = sum(1 for skill in ds_skills if skill in job_text)
        relevance_score += ds_matches * 20
        
        # Score 2: General skill matching
        skill_matches = sum(1 for skill in candidate_skills if skill in job_text)
        relevance_score += skill_matches * 10
        
        # Score 3: Experience level (Sreecharan has 2 years)
        if 'junior' in job_title or 'associate' in job_title or 'entry' in job_title:
            relevance_score += 25  # Good for 2 years experience
        elif 'senior' not in job_title and 'lead' not in job_title:
            relevance_score += 15  # Mid-level jobs
        
        # Score 4: Title relevance
        if 'data scientist' in job_title:
            relevance_score += 30
        elif 'data' in job_title:
            relevance_score += 20
        elif 'python' in job_title:
            relevance_score += 15
        
        # Score 5: Bangalore location bonus
        job_location = job.get('location', '').lower()
        if 'bangalore' in job_location or 'bengaluru' in job_location:
            relevance_score += 10
        
        # Score 6: Salary disclosure bonus
        if job.get('salary_range') and 'not disclosed' not in job.get('salary_range', '').lower():
            relevance_score += 5
        
        job['relevance_score'] = relevance_score
        ranked_jobs.append(job)
    
    # Sort by relevance score
    ranked_jobs.sort(key=lambda x: x.get('relevance_score', 0), reverse=True)
    
    return ranked_jobs

